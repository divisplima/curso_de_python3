frase = "Curso em Vídeo Python"
print(frase[3])                     # Mostra o índice de número 3
print(frase[3:13])                  # Mostra as substrings do 3 ao 12
print(frase[:13])                   # Mostra do início até o 12
print(frase[13:])                   # Mostra do índice 13 até o final
print(frase[1:15:2])                # Mostra a cadeia de caracteres do 1 ao 14 pulando de dois em dois
